CREATE DATABASE futBrain;
USE futBrain;

CREATE TABLE usuario(
    idUsuario INT PRIMARY KEY AUTO_INCREMENT,
    nome VARCHAR(45),
    usuario VARCHAR(45),
    email VARCHAR(45),
    senha VARCHAR(45)  
);

CREATE TABLE quizzes(
    idQuiz INT PRIMARY KEY AUTO_INCREMENT,
    liga VARCHAR(45) UNIQUE
);

CREATE TABLE resultado (
    idResultado INT AUTO_INCREMENT PRIMARY KEY,
    fkUsuario INT,
    fkQuiz INT,
    pontos INT,
    dtQuiz DATETIME DEFAULT CURRENT_TIMESTAMP,
    tempo_segundos INT,
    FOREIGN KEY (fkUsuario) REFERENCES Usuario(idUsuario),
    FOREIGN KEY (fkQuiz) REFERENCES Quizzes(idQuiz)
);

CREATE TABLE perguntas(
    idPergunta INT AUTO_INCREMENT,
    fkQuiz INT,
    descricao VARCHAR(350),
    Foreign Key (fkQuiz) REFERENCES Quizzes(idQuiz),
    PRIMARY KEY(idPergunta, fkQuiz)
);

CREATE TABLE alternativas(
    idAlternativa INT AUTO_INCREMENT,
    fkPergunta INT,
    resposta VARCHAR(80),
    correta TINYINT,
    Foreign Key (fkPergunta) REFERENCES Perguntas(idPergunta),
    PRIMARY KEY(idAlternativa, fkPergunta)
);

